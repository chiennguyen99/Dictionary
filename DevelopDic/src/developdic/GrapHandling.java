    private void buttonaddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonaddMouseClicked
        // TODO add your handling code here:
        String Spelling = new String(); 
        String explain = new String(); 
        Dictionary1 Dicdelete = new Dictionary1(); 
        if ((!stringkeySpelling.equals("Search...") && !stringkeySpelling.equals("")) && Outputexplain.getSelectedIndex() == 0){
            Spelling = stringkeySpelling; 
            explain = Outputexplain.getSelectedValue();
        }
        else {
            Spelling = doispelling.getText(); 
            explain = doiexplain.getText();
        }
        int check = Dicdelete.BinarySearch(Spelling);
       
        if (check == -1){
            if ((!Spelling.equals("Spelling...") && !Spelling.equals("")) && (!explain.equals("Explain...") && !explain.equals("")) ){
           
            int k = JOptionPane.showConfirmDialog(rootPane, "Bạn muốn thêm từ này ?", "Dictionary", 0); 
            
            if (k == 0){
                
                Word W = new Word(); 
                
                W.setSpelling(Spelling); 
                
                W.setExplain(explain);
                
                W.setphienam("");
                
                ListHandling.AddWordToList(W);
                ListHandling.Sort();
                ListHandling.WriteListToFile();
                String s = String.valueOf(ListHandling.ReturnSizeList()); 
                UpDateWords.setText("Words" + ": " + s);
            }
            doispelling.setText("Spelling...");
            doiexplain.setText("Explain...");
            }else {
                doispelling.setText("Spelling...");
                doiexplain.setText("Explain...");
            }
        }else {  
            JOptionPane.showMessageDialog(rootPane, "Từ đã có trong từ điển !", "MyDictionary", HEIGHT);
        } 
    }//GEN-LAST:event_buttonaddMouseClicked

