    private void butEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butEditMouseClicked
        // TODO add your handling code here:
        String s1 = new String(); 
        String s2 = new String(); 
        Word W = new Word(); 
        Dictionary1 D = new Dictionary1();
        s1 = doispelling.getText(); 
        s2 = doiexplain.getText();       
    
        if (OutputSpelling.getSelectedIndex() != -1 && !s1.equals("Spelling...") && !s2.equals("Explain...") && !s1.equals("") && !s2.equals("") && indexEdit != -1){          
            int IC = D.BinarySearch(L.ReturnWordSpelling(indexEdit)); 
            W.setSpelling(s1);
            W.setExplain(s2);

            W.setphienam(ListHandling.ReturnWordPhienAm(IC));
            ListHandling.ChangeWord(W, IC);
            ListHandling.WriteListToFile();
            doispelling.setText("Spelling...");
            doiexplain.setText("Explain...");
            indexEdit = -1; 
            JOptionPane.showMessageDialog(rootPane, "Từ đã được sửa.^_^.");
        }
        else if (OutputSpelling.getSelectedIndex() == -1 && !s1.equals("Spelling...") && !s2.equals("Explain...") && !s1.equals("") && !s2.equals("")){
         
            int IC = D.BinarySearch(s1); 
            if (IC != -1){
                W.setSpelling(s1);
                W.setExplain(s2); 
                W.setphienam(ListHandling.ReturnWordPhienAm(IC));
                ListHandling.ChangeWord(W, IC);
                ListHandling.WriteListToFile();
                doispelling.setText("Spelling...");
                doiexplain.setText("Explain...");
            }else {
                JOptionPane.showMessageDialog(rootPane, "Từ này không có trong từ điển !");
                doispelling.setText("Spelling...");
                doiexplain.setText("Explain...");
            }
        }
        else {
            doispelling.setText("Spelling...");
            doiexplain.setText("Explain...");
        }

    }//GEN-LAST:event_butEditMouseClicked

