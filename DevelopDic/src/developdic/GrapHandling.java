    String TranslateGoogle(String tagetlanguage, String s){
        String s1 = ""; 
        try{
            s1 = GoogleTranslate.translate(tagetlanguage, s); 
        }catch(IOException e){
//            System.out.println(e.getLocalizedMessage());
              System.out.println("Not Internet !!!");
        }
        if (!s1.equals("")){
            return s1; 
        }
        else {
            return "Không kết nối"; 
        }
    }
    
